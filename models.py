def get_Model(name, dataset_name):

    if dataset_name == 'mnist':
        in_channels = 1
    else:
        in_channels =3

        
    if name == "mlp":
        model = Mlp(n_classes=10, dropout=False)
    elif name == "resnet34":
        model = ResNet([3, 4, 6, 3], num_classes=10, in_channels=in_channels) # tf: [5,5,5] ResNet 32 (efficientnet oder mobilenet version 2)
    elif name == "resnet34_100":
        model = ResNet([3, 4, 6, 3], num_classes=100, in_channels=in_channels)
    elif name == "resnet32":
        model = ResNet_32(BasicBlock, [5, 5, 5])
    elif name == "mobileNetV2":
        model = MobileNetV2()

    return model





